def build_user_context(
    user_data: dict,
    memories: list,
    mood_history: list,
    persons: list = None,
    recent_events: list = None,
    upcoming_dates: list = None,
) -> str:
    """Build rich user context for the system prompt.

    This is the "friend's memory" - everything the bot knows about the person.
    """
    sections = []

    # Identity
    identity = []
    if user_data.get("name"):
        identity.append(f"–ò–º—è: {user_data['name']}")

    profile = user_data.get("profile", {}) or {}
    if profile.get("age"):
        identity.append(f"–í–æ–∑—Ä–∞—Å—Ç: {profile['age']}")
