def build_additional_context(
    time_of_day: str,
    days_since_last_chat: int,
    conversation_summaries: list,
    current_mood: dict = None,
) -> str:
    """Build additional context for the conversation."""
    parts = []

    # Time awareness
    time_notes = {
        "morning": "–£—Ç—Ä–æ",
        "afternoon": "–î–µ–Ω—å",
        "evening": "–í–µ—á–µ—Ä",
        "night": "–ù–æ—á—å (–º–æ–∂–µ—Ç –±—ã—Ç—å –±–µ—Å—Å–æ–Ω–Ω–∏—Ü–∞, –æ–¥–∏–Ω–æ—á–µ—Å—Ç–≤–æ)",
    }
    if time_of_day in time_notes:
        parts.append(f"–í—Ä–µ–º—è: {time_notes[time_of_day]}")

    # Return context
    if days_since_last_chat > 14:
        parts.append(f"–ù–µ –æ–±—â–∞–ª–∏—Å—å {days_since_last_chat} –¥–Ω–µ–π ‚Äî —Å—Ç–æ–∏—Ç –º—è–≥–∫–æ —É–∑–Ω–∞—Ç—å –∫–∞–∫ –¥–µ–ª–∞")
    elif days_since_last_chat > 3:
        parts.append(f"–ù–µ –æ–±—â–∞–ª–∏—Å—å {days_since_last_chat} –¥–Ω–µ–π")

    # Previous conversations
    if conversation_summaries:
        parts.append("\n–ò–∑ –ø—Ä–æ—à–ª—ã—Ö —Ä–∞–∑–≥–æ–≤–æ—Ä–æ–≤:")
        for summary in conversation_summaries[:3]:
            parts.append(f"‚Ä¢ {summary}")

    # Current mood if detected
    if current_mood:
        mood_score = current_mood.get("mood_score", 5)
        emotion = current_mood.get("primary_emotion", "")
        need = current_mood.get("emotional_need", "")

        if mood_score <= 3:
            parts.append(f"\n‚ö†Ô∏è –°–µ–π—á–∞—Å: {emotion}, –Ω—É–∂–Ω–æ {need}")
        elif emotion:
            parts.append(f"\n–°–µ–π—á–∞—Å: {emotion}")

    return "\n".join(parts) if parts else ""


